        cMap.setJavaName( "ScriptRole" );
        cMap.setAutoIncrement(false);
        cMap.setProtected(false);
        cMap.setDescription("from where to use it: from field config, workflow etc.");
        cMap.setInheritance("false");
        cMap.setPosition(9);
        tMap.addColumn(cMap);
  // ------------- Column: CLAZZNAME --------------------
        cMap = new ColumnMap( "CLAZZNAME", tMap);
        cMap.setType( "" );
        cMap.setTorqueType( "VARCHAR" );
        cMap.setUsePrimitive(false);
        cMap.setPrimaryKey(false);
        cMap.setNotNull(false);
        cMap.setJavaName( "ClazzName" );
        cMap.setAutoIncrement(false);
        cMap.setProtected(false);
        cMap.setInheritance("false");
        cMap.setSize( 253 );
        cMap.setPosition(10);
        tMap.addColumn(cMap);
  // ------------- Column: SOURCECODE --------------------
        cMap = new ColumnMap( "SOURCECODE", tMap);
        cMap.setType( "" );
        cMap.setTorqueType( "VARCHAR" );
        cMap.setUsePrimitive(false);
